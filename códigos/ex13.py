from sys import argv # É como você adiciona recursos ao script a partir do conjunto de recursos do Python. Ao invés de lhe dar todos os recursos de uma só vez, o python pede que você informe o que pretende usar. 
# A parte do argv é a variável do argumento. Essa variável retém os argumentos passados para o script do Python quando você o executa.

script, first, second, third = argv # Ele é atribuído às quatro variáveis com as quais você pode trabalhar: script, first, second, e third.


print("The script is called:", script) # O script é chamado
print('Your first variable is:', first) # Sua primeira variável é
print('Your second variable is:', second) # Sua segunda variável é
print('Your third variable is:', third) # Sua terceira variável é

